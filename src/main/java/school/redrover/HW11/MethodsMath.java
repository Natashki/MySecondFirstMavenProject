package school.redrover.HW11;

public class MethodsMath {
    public static int plus(int x, int y){
        return x + y;
    }

    public static int minus(int x, int y){
        return x - y;
    }

    public static int multiply(int x, int y){
        return x * y;
    }

    public static int divide(int x, int y){
        return x / y;
    }
}
